# Logging Policy

This project respects GitHub Actions logging guidelines and fair use policies.

- **No sensitive information** (e.g., Telegram Bot tokens, chat IDs, or API keys) is printed or logged.  
- Logs include only minimal information necessary for workflow status, FFmpeg streaming progress, and debugging.  
- We continuously monitor logs to ensure no abusive, malicious, or unauthorized streaming activity occurs.  
- This project complies fully with GitHub’s Terms of Service, Telegram’s Terms of Service, and fair use policies.  

If you have concerns about logs or require additional information, please contact the repository owner.

---

*This policy is designed to maintain transparency and trust with GitHub, Telegram, and users.*
